    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Boton INICIAS SESION:
        
        String nombre = this.jTextField1.getText();
        String contra = this.jPasswordField1.getText();
        String tipo = "";
        
        JSONArray arrayUsuarios = this.a.arrayUsers();
        
        boolean valido = false;
        
        if(arrayUsuarios != null){
            JSONArray arr = (JSONArray) arrayUsuarios.get(0);
            for(int i = 0; i < arr.size(); i++){                
                JSONObject user = (JSONObject) arr.get(i);
                String nom = user.get("nom").toString();
                String con = user.get("con").toString();
                
                if(nom.equals(nombre) && con.equals(contra)){
                    tipo = user.get("tipo").toString();
                    valido = true;
                    break;
                }else{
                    valido = false;
                }
            }
        }

        if(valido){
            if(tipo.equals("1")){
                Administrador admin = new Administrador();
                admin.setLocationRelativeTo(null);
                admin.setVisible(true);            
                this.dispose();
            }else if (tipo.equals("2")){
                General gen = new General();
                gen.setLocationRelativeTo(null);
                gen.setVisible(true);
                this.dispose();
            }           
        }else{
            this.jTextField1.setText("");
            this.jPasswordField1.setText("");
            this.Alerta("Usuario o ContraseÃ±a no validos!!");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    
